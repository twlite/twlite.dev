import fs from "node:fs";
import path from "node:path";
import matter from "gray-matter";
import readingTime from "reading-time";

const watcherKey = Symbol.for("blog.staticPostsWatcher");

const toPosixPath = (value) => value.split(path.sep).join("/");

export function readBlogPosts({
  contentDirectory = path.join(process.cwd(), "src/content/blog"),
} = {}) {
  if (!fs.existsSync(contentDirectory)) {
    return [];
  }

  return fs
    .readdirSync(contentDirectory)
    .filter((fileName) => fileName.endsWith(".mdx") || fileName.endsWith(".md"))
    .map((fileName) => {
      const slug = fileName.replace(/\.mdx?$/, "");
      const filePath = path.join(contentDirectory, fileName);
      const fileContents = fs.readFileSync(filePath, "utf8");
      const { data, content } = matter(fileContents);
      const stats = readingTime(content);

      return {
        slug,
        title: data.title,
        date: data.date,
        description: data.description || "",
        content,
        readingTime: Math.ceil(stats.minutes).toString(),
        tags: data.tags || [],
      };
    })
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
}

export function renderBlogPostsModule(posts) {
  const entries = posts
    .map((post) => {
      return `  ${JSON.stringify(post.slug)}: ${JSON.stringify(post, null, 2).replace(/\n/g, "\n  ")},`;
    })
    .join("\n");
  const orderedPosts = posts
    .map((post) => `  postsBySlug[${JSON.stringify(post.slug)}],`)
    .join("\n");

  return `// This file is generated by scripts/static-blog-posts.mjs.
// Do not edit this file directly.

import type { Post } from "@/lib/posts";

export const postsBySlug: Record<string, Post> = {
${entries}
};

export const allPosts: Post[] = [
${orderedPosts}
];
`;
}

export function renderBlogPostSummariesModule(posts) {
  const summaries = posts.map((post) => {
    const summary = { ...post };
    delete summary.content;
    return summary;
  });
  const entries = summaries
    .map((post) => {
      return `  ${JSON.stringify(post.slug)}: ${JSON.stringify(post, null, 2).replace(/\n/g, "\n  ")},`;
    })
    .join("\n");
  const orderedPosts = summaries
    .map((post) => `  postSummariesBySlug[${JSON.stringify(post.slug)}],`)
    .join("\n");

  return `// This file is generated by scripts/static-blog-posts.mjs.
// Do not edit this file directly.

import type { PostSummary } from "@/lib/post-summaries";

export const postSummariesBySlug: Record<string, PostSummary> = {
${entries}
};

export const allPostSummaries: PostSummary[] = [
${orderedPosts}
];
`;
}

export function compileBlogPosts({
  contentDirectory = path.join(process.cwd(), "src/content/blog"),
  outputFile = path.join(process.cwd(), "src/generated/blog-posts.ts"),
  summariesOutputFile = path.join(
    process.cwd(),
    "src/generated/blog-post-summaries.ts",
  ),
} = {}) {
  const posts = readBlogPosts({ contentDirectory });
  const source = renderBlogPostsModule(posts);
  const summariesSource = renderBlogPostSummariesModule(posts);

  fs.mkdirSync(path.dirname(outputFile), { recursive: true });

  if (
    fs.existsSync(outputFile) &&
    fs.readFileSync(outputFile, "utf8") === source
  ) {
    if (
      fs.existsSync(summariesOutputFile) &&
      fs.readFileSync(summariesOutputFile, "utf8") === summariesSource
    ) {
      return posts;
    }
  } else {
    fs.writeFileSync(outputFile, source);
  }

  fs.writeFileSync(summariesOutputFile, summariesSource);
  return posts;
}

export function withStaticBlogPosts(nextConfig = {}) {
  const contentDirectory = path.join(process.cwd(), "src/content/blog");
  const outputFile = path.join(process.cwd(), "src/generated/blog-posts.ts");
  const summariesOutputFile = path.join(
    process.cwd(),
    "src/generated/blog-post-summaries.ts",
  );
  const generate = () =>
    compileBlogPosts({ contentDirectory, outputFile, summariesOutputFile });

  generate();

  if (process.env.NODE_ENV === "development" && !globalThis[watcherKey]) {
    let timeout;

    const scheduleGenerate = () => {
      clearTimeout(timeout);
      timeout = setTimeout(generate, 50);
    };

    const watcher = fs.watch(contentDirectory, scheduleGenerate);
    globalThis[watcherKey] = watcher;

    process.once("exit", () => watcher.close());
  }

  return nextConfig;
}

export const blogPostsGeneratedPath = toPosixPath(
  path.relative(
    process.cwd(),
    path.join(process.cwd(), "src/generated/blog-posts.ts"),
  ),
);

import fs from "node:fs";
import path from "node:path";

const watcherKey = Symbol.for("blog.staticPhotographsWatcher");
const imageExtensions = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif"]);

const titleFromFileName = (fileName) => {
  const name = path.basename(fileName, path.extname(fileName));

  return name
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
};

const readJpegDimensions = (buffer) => {
  let offset = 2;

  while (offset < buffer.length) {
    if (buffer[offset] !== 0xff) {
      break;
    }

    const marker = buffer[offset + 1];
    const length = buffer.readUInt16BE(offset + 2);

    if (
      (marker >= 0xc0 && marker <= 0xc3) ||
      (marker >= 0xc5 && marker <= 0xc7) ||
      (marker >= 0xc9 && marker <= 0xcb) ||
      (marker >= 0xcd && marker <= 0xcf)
    ) {
      return {
        height: buffer.readUInt16BE(offset + 5),
        width: buffer.readUInt16BE(offset + 7),
      };
    }

    offset += 2 + length;
  }

  return null;
};

const readPngDimensions = (buffer) => {
  if (buffer.toString("ascii", 12, 16) !== "IHDR") {
    return null;
  }

  return {
    width: buffer.readUInt32BE(16),
    height: buffer.readUInt32BE(20),
  };
};

const readImageDimensions = (filePath) => {
  const buffer = fs.readFileSync(filePath);
  const extension = path.extname(filePath).toLowerCase();

  if (extension === ".jpg" || extension === ".jpeg") {
    return readJpegDimensions(buffer);
  }

  if (extension === ".png") {
    return readPngDimensions(buffer);
  }

  return null;
};

export function readPhotographs({
  photographsDirectory = path.join(process.cwd(), "public/photographs"),
} = {}) {
  if (!fs.existsSync(photographsDirectory)) {
    return [];
  }

  return fs
    .readdirSync(photographsDirectory)
    .filter((fileName) => imageExtensions.has(path.extname(fileName).toLowerCase()))
    .sort((a, b) => a.localeCompare(b))
    .map((fileName) => {
      const filePath = path.join(photographsDirectory, fileName);
      const stats = fs.statSync(filePath);
      const slug = path.basename(fileName, path.extname(fileName));
      const dimensions = readImageDimensions(filePath);

      return {
        slug,
        title: titleFromFileName(fileName),
        src: `/photographs/${fileName}`,
        fileName,
        width: dimensions?.width || 1,
        height: dimensions?.height || 1,
        size: stats.size,
        updatedAt: stats.mtime.toISOString(),
      };
    });
}

export function renderPhotographsModule(photographs) {
  const entries = photographs
    .map((photograph) => `  ${JSON.stringify(photograph)},`)
    .join("\n");

  return `// This file is generated by scripts/static-photographs.mjs.
// Do not edit this file directly.

import type { Photograph } from "@/lib/photographs";

export const allPhotographs: Photograph[] = [
${entries}
];
`;
}

export function compilePhotographs({
  photographsDirectory = path.join(process.cwd(), "public/photographs"),
  outputFile = path.join(process.cwd(), "src/generated/photographs.ts"),
} = {}) {
  const photographs = readPhotographs({ photographsDirectory });
  const source = renderPhotographsModule(photographs);

  fs.mkdirSync(path.dirname(outputFile), { recursive: true });

  if (fs.existsSync(outputFile) && fs.readFileSync(outputFile, "utf8") === source) {
    return photographs;
  }

  fs.writeFileSync(outputFile, source);
  return photographs;
}

export function withStaticPhotographs(nextConfig = {}) {
  const photographsDirectory = path.join(process.cwd(), "public/photographs");
  const outputFile = path.join(process.cwd(), "src/generated/photographs.ts");
  const generate = () => compilePhotographs({ photographsDirectory, outputFile });

  generate();

  if (process.env.NODE_ENV === "development" && !globalThis[watcherKey]) {
    let timeout;

    const scheduleGenerate = () => {
      clearTimeout(timeout);
      timeout = setTimeout(generate, 50);
    };

    const watcher = fs.watch(photographsDirectory, scheduleGenerate);
    globalThis[watcherKey] = watcher;

    process.once("exit", () => watcher.close());
  }

  return nextConfig;
}
